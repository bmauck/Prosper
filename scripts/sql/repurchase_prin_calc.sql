								(co.PrincipalBalance + co.InterestBalance + co.LateFeesBalance + co.NSFFeesBalance)
	,ChargeOffPrincipal		=	CAST(ln.OwnershipShare AS DECIMAL(20,10)) / CAST(lo.OriginalAmountBorrowed AS DECIMAL(20,10))
								*
								co.PrincipalBalance
	,ChargeOffInterest		=	CAST(ln.OwnershipShare AS DECIMAL(20,10)) / CAST(lo.OriginalAmountBorrowed AS DECIMAL(20,10))
								*
								co.InterestBalance
	,ChargeOffLateFees		=	CAST(ln.OwnershipShare AS DECIMAL(20,10)) / CAST(lo.OriginalAmountBorrowed AS DECIMAL(20,10))
								*
								co.LateFeesBalance
---------------------------Bankruptcy---------------------------
	,BankruptcyFiledDate	= bk.DateOfFiling
	,BankruptcyStatus		= bk.[Description]
FROM CircleOne.dbo.Listings (NOLOCK) li
JOIN CircleOne.dbo.Loans (NOLOCK) lo ON lo.LoanID = li.LoanID
JOIN CircleOne.dbo.LoanNote (NOLOCK) ln ON ln.LoanID = lo.LoanID
JOIN CircleOne.dbo.ProsperRatingType (NOLOCK) prt ON prt.ProsperRatingTypeID = li.ProsperRatingTypeID
JOIN CircleOne.dbo.ProductSpecs (NOLOCK) ps ON ps.ProductSpecID = li.ProductSpecID
JOIN @LoanNotes l ON l.LoanNoteID = ln.LoanNoteID
LEFT JOIN CircleOne.dbo.LoanDetail (NOLOCK) ld ON ld.LoanID = lo.LoanID
	AND ld.VersionValidBit = 1
	AND ld.VersionEndDate IS NULL
LEFT JOIN CircleOne.dbo.LoanStatusTypes (NOLOCK) lst ON lst.ID = ld.LoanStatusTypesID
OUTER APPLY (
	SELECT TOP 1
		lcod.ChargeOffDate
		,lds.PrincipalBalance
		,lds.InterestBalance
		,lds.LateFeesBalance

	,lo.LoanID
	,ln.LoanNoteID
	,LoanNoteDisplay		=	CAST(ln.LoanID AS VARCHAR(MAX)) + '-' + CAST(ln.SeqNo AS VARCHAR(MAX))
	,OriginalInvestment		=	ln.OwnershipShare
	,LoanAmount				=	lo.OriginalAmountBorrowed
	,ProRataShare			=	CAST(ln.OwnershipShare AS DECIMAL(20,10)) / CAST(lo.OriginalAmountBorrowed AS DECIMAL(20,10))
--------------------------LoanDetails---------------------------
	,ld.BalDate
	,DaysforAccrual			=	DATEDIFF(DD,ld.BalDate,@AsOfDate)
	,ld.LoanStatusTypesID
	,LoanStatusDescription	=	lst.Name
	,DaysPastDue			=	ld.DPD
	,MaturityDate			=	ld.ExpectedMaturityDate
	,ld.NextPaymentDueDate
	,BorrowerRate			=	ld.BorrowerStatedInterestRate
	,LoanClosedDate			=	ld.DateClosed
	,AgeInMonths			=	DATEDIFF(M,lo.OriginationDate,@AsOfDate)
----------------------------Accruals----------------------------
	,PrincipalBalance		=	CAST(ln.OwnershipShare AS DECIMAL(20,10)) / CAST(lo.OriginalAmountBorrowed AS DECIMAL(20,10))
								* 
								CAST(ld.PrinBal AS DECIMAL(20,10))
	,AccruedInterest		=	CAST(ln.OwnershipShare AS DECIMAL(20,10)) / CAST(lo.OriginalAmountBorrowed AS DECIMAL(20,10))
								*
								CAST(
									CASE
										WHEN ld.LoanStatusTypesID IN (0,1)
											THEN ld.IntBal + (ld.IntBalDailyAccrual * DATEDIFF(DD,ld.BalDate,@AsOfDate))
										ELSE ld.IntBal
									END
								AS DECIMAL(20,10))
	,AccruedLatefee			=	CAST(ln.OwnershipShare AS DECIMAL(20,10)) / CAST(lo.OriginalAmountBorrowed AS DECIMAL(20,10))
								* 
								CAST(ld.LateFeeBal AS DECIMAL(20,10))
	,AccruedNSFFee			=	CAST(ln.OwnershipShare AS DECIMAL(20,10)) / CAST(lo.OriginalAmountBorrowed AS DECIMAL(20,10))
								* 
								CAST(ld.NSFFeeBal AS DECIMAL(20,10))
	,AccruedSvcFee			=	CAST(ln.OwnershipShare AS DECIMAL(20,10)) / CAST(lo.OriginalAmountBorrowed AS DECIMAL(20,10))
								*
								CAST(
									CASE
										WHEN ld.LoanStatusTypesID IN (0,1)
											THEN ld.IntBal + (ld.IntBalDailyAccrual * DATEDIFF(DD,ld.BalDate,@AsOfDate))
										ELSE ld.IntBal
									END
									*
									--NOTE: ServiceFeePercent Hard-Coded as 0.01075
									ISNULL(		CAST(0.01075 AS DECIMAL(20,10)) / CAST(NULLIF(ld.BorrowerStatedInterestRate,0) AS DECIMAL(20,10))	, 0.00)
								AS DECIMAL(20,10))
---------------------------Attributes---------------------------
	,ProsperRating			=	prt.RatingCode
	,Term					=	ps.AmortizationMonths
